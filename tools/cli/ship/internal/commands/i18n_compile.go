package commands

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
	"unicode"
)

func runI18nCompile(args []string, d I18nDeps, root string) int {
	for _, arg := range args {
		if arg == "--help" || arg == "-h" {
			fmt.Fprintln(d.Out, "usage: ship i18n:compile")
			return 0
		}
	}
	if len(args) > 0 {
		fmt.Fprintln(d.Err, "usage: ship i18n:compile")
		return 1
	}

	localesDir := filepath.Join(root, "locales")
	basePath, err := resolveEnglishLocalePath(localesDir)
	if err != nil {
		fmt.Fprintf(d.Err, "i18n:compile failed to read baseline locale: %v\n", err)
		return 1
	}
	base, err := loadLocaleFlatFromFile(basePath)
	if err != nil {
		fmt.Fprintf(d.Err, "i18n:compile failed to read %s: %v\n", filepath.Base(basePath), err)
		return 1
	}

	keys := make([]string, 0, len(base))
	for key := range base {
		key = strings.TrimSpace(key)
		if key == "" {
			continue
		}
		keys = append(keys, key)
	}
	sort.Strings(keys)

	goPath := filepath.Join(root, "app", "i18nkeys", "keys_gen.go")
	if err := os.MkdirAll(filepath.Dir(goPath), 0o755); err != nil {
		fmt.Fprintf(d.Err, "i18n:compile failed to create %s: %v\n", filepath.ToSlash(filepath.Dir(goPath)), err)
		return 1
	}
	if err := os.WriteFile(goPath, []byte(renderI18nGoKeys(keys)), 0o644); err != nil {
		fmt.Fprintf(d.Err, "i18n:compile failed to write %s: %v\n", filepath.ToSlash(goPath), err)
		return 1
	}

	tsPath := filepath.Join(root, "frontend", "islands", "i18n-keys.ts")
	if err := os.MkdirAll(filepath.Dir(tsPath), 0o755); err != nil {
		fmt.Fprintf(d.Err, "i18n:compile failed to create %s: %v\n", filepath.ToSlash(filepath.Dir(tsPath)), err)
		return 1
	}
	if err := os.WriteFile(tsPath, []byte(renderI18nTSKeys(keys)), 0o644); err != nil {
		fmt.Fprintf(d.Err, "i18n:compile failed to write %s: %v\n", filepath.ToSlash(tsPath), err)
		return 1
	}

	fmt.Fprintln(d.Out, "i18n:compile complete.")
	fmt.Fprintf(d.Out, "  keys: %d\n", len(keys))
	fmt.Fprintf(d.Out, "  go: %s\n", filepath.ToSlash(goPath))
	fmt.Fprintf(d.Out, "  ts: %s\n", filepath.ToSlash(tsPath))
	return 0
}

func renderI18nGoKeys(keys []string) string {
	used := map[string]struct{}{}
	b := strings.Builder{}
	b.WriteString("// Code generated by ship i18n:compile. DO NOT EDIT.\n")
	b.WriteString("package i18nkeys\n\n")
	if len(keys) == 0 {
		b.WriteString("// No i18n keys found in baseline locale.\n")
		return b.String()
	}
	b.WriteString("const (\n")
	for _, key := range keys {
		baseName := goI18nConstName(key)
		name := uniqueGoConstName(baseName, used)
		b.WriteString(fmt.Sprintf("\t%s = %s\n", name, strconv.Quote(key)))
	}
	b.WriteString(")\n")
	return b.String()
}

func renderI18nTSKeys(keys []string) string {
	b := strings.Builder{}
	b.WriteString("// Code generated by ship i18n:compile. DO NOT EDIT.\n")
	b.WriteString("export const i18nKeys = {\n")
	for _, key := range keys {
		q := strconv.Quote(key)
		b.WriteString(fmt.Sprintf("  %s: %s,\n", q, q))
	}
	b.WriteString("} as const;\n\n")
	b.WriteString("export type I18nKey = keyof typeof i18nKeys;\n")
	return b.String()
}

func goI18nConstName(key string) string {
	parts := strings.FieldsFunc(key, func(r rune) bool {
		return !(unicode.IsLetter(r) || unicode.IsDigit(r))
	})
	if len(parts) == 0 {
		return "KeyMessage"
	}
	builder := strings.Builder{}
	builder.WriteString("Key")
	for _, part := range parts {
		if part == "" {
			continue
		}
		runes := []rune(strings.ToLower(part))
		runes[0] = unicode.ToUpper(runes[0])
		builder.WriteString(string(runes))
	}
	name := builder.String()
	if name == "Key" {
		return "KeyMessage"
	}
	return name
}

func uniqueGoConstName(base string, used map[string]struct{}) string {
	name := base
	for i := 2; ; i++ {
		if _, exists := used[name]; !exists {
			used[name] = struct{}{}
			return name
		}
		name = fmt.Sprintf("%s%d", base, i)
	}
}
