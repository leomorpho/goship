func TestShipDBGenerateFromCleanProjectIsIdempotent(t *testing.T) {
	if _, err := exec.LookPath("go"); err != nil {
		t.Skip("go toolchain not available")
	}

	repoRoot := mustRepoRootFromFile(t)
	shipBin := filepath.Join(t.TempDir(), "ship")

	buildCtx, buildCancel := context.WithTimeout(context.Background(), 2*time.Minute)
	defer buildCancel()

	build := exec.CommandContext(buildCtx, "go", "build", "-o", shipBin, "./tools/cli/ship/cmd/ship")
	build.Dir = repoRoot
	if out, err := build.CombinedOutput(); err != nil {
		t.Fatalf("build ship binary: %v: %s", err, string(out))
	}

	workspace := filepath.Join(t.TempDir(), "workspace")
	if err := os.MkdirAll(workspace, 0o755); err != nil {
		t.Fatalf("mkdir workspace: %v", err)
	}

	fakeBin := filepath.Join(t.TempDir(), "fake-bin")
	if err := os.MkdirAll(fakeBin, 0o755); err != nil {
		t.Fatalf("mkdir fake bin: %v", err)
	}
	writeFakeBobgen(t, fakeBin)

	runShip := func(dir string, env []string, args ...string) string {
		t.Helper()
		ctx, cancel := context.WithTimeout(context.Background(), 2*time.Minute)
		defer cancel()
		cmd := exec.CommandContext(ctx, shipBin, args...)
		cmd.Dir = dir
		cmd.Env = append(os.Environ(), env...)
		out, err := cmd.CombinedOutput()
		msg := string(out)
		if err != nil {
			if isLikelyEnvironmentConstraint(msg) {
				t.Skipf("skipping db:generate integration (environment/toolchain constraint): %v: %s", err, msg)
			}
			var exitErr *exec.ExitError
			if errors.As(err, &exitErr) {
				t.Fatalf("ship %s failed (%d): %s", strings.Join(args, " "), exitErr.ExitCode(), msg)
			}
			t.Fatalf("ship %s failed: %v: %s", strings.Join(args, " "), err, msg)
		}
		return msg
	}

	runShip(workspace, nil, "new", "demo", "--module", "example.com/demo")
	projectRoot := filepath.Join(workspace, "demo")
	pathEnv := []string{"PATH=" + fakeBin + string(os.PathListSeparator) + os.Getenv("PATH")}

	if _, err := os.Stat(filepath.Join(projectRoot, "db", "bobgen.yaml")); err != nil {
		t.Fatalf("expected bobgen config in new project: %v", err)
	}

	out1 := runShip(projectRoot, pathEnv, "db:generate")
	if !strings.Contains(out1, "DB generate plan") {
		t.Fatalf("expected generate plan output, got: %s", out1)
	}

	generatedFile := filepath.Join(projectRoot, "db", "gen", "models.go")
	firstBytes, err := os.ReadFile(generatedFile)
	if err != nil {
		t.Fatalf("read generated file after first run: %v", err)
	}

	out2 := runShip(projectRoot, pathEnv, "db:generate")
	if !strings.Contains(out2, "DB generate plan") {
		t.Fatalf("expected generate plan output on second run, got: %s", out2)
	}

	secondBytes, err := os.ReadFile(generatedFile)
	if err != nil {
		t.Fatalf("read generated file after second run: %v", err)
	}
	if string(firstBytes) != string(secondBytes) {
		t.Fatalf("db:generate is not idempotent: generated content changed between runs")
	}
}

func writeFakeBobgen(t *testing.T, binDir string) {
	t.Helper()
	script := `#!/usr/bin/env bash
set -euo pipefail
if [ "${1:-}" != "-c" ] || [ -z "${2:-}" ]; then
  echo "usage: bobgen-sql -c <config>" >&2
  exit 2
fi
mkdir -p db/gen
cat > db/gen/models.go <<'EOF'
// Code generated by fake bobgen in integration test. DO NOT EDIT.
package gen
EOF
`
	path := filepath.Join(binDir, "bobgen-sql")
	if err := os.WriteFile(path, []byte(script), 0o755); err != nil {
		t.Fatalf("write fake bobgen: %v", err)
	}
}
